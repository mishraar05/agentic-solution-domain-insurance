# confidence.py
with open(os.path.join(SRC_DIR, "confidence.py"), "w", encoding="utf-8") as f:
    f.write('"""Governed confidence calculation from weighted components."""\n')
    f.write('from dataclasses import dataclass\n\n')
    f.write('FORMULA_VERSION = "1.0.0"\n')
    f.write('WEIGHTS = {"naming_strength": 0.35, "type_strength": 0.20, "relationship_strength": 0.25, "cots_match_strength": 0.10, "standard_consistency": 0.10}\n')
    f.write('EVIDENCE_COVERAGE_FLOOR = 0.60\n\n\n')
    f.write('@dataclass\n')
    f.write('class ConfidenceResult:\n')
    f.write('    score: float\n')
    f.write('    components: dict\n')
    f.write('    evidence_coverage: float\n')
    f.write('    formula_version: str\n')
    f.write('    confidence_reason: str\n')
    f.write('    raw_weighted_sum: float\n')
    f.write('    normalized_score: float\n\n\n')
    f.write('def compute_confidence(naming_strength, type_strength, relationship_strength, cots_match_strength=None, standard_consistency=None):\n')
    f.write('    """Compute governed confidence score from components."""\n')
    f.write('    components = {\n')
    f.write('        "naming_strength": {"value": naming_strength, "availability": "AVAILABLE", "weight": WEIGHTS["naming_strength"]},\n')
    f.write('        "type_strength": {"value": type_strength, "availability": "AVAILABLE", "weight": WEIGHTS["type_strength"]},\n')
    f.write('        "relationship_strength": {"value": relationship_strength, "availability": "AVAILABLE", "weight": WEIGHTS["relationship_strength"]},\n')
    f.write('    }\n')
    f.write('    if cots_match_strength is not None:\n')
    f.write('        components["cots_match_strength"] = {"value": cots_match_strength, "availability": "AVAILABLE", "weight": WEIGHTS["cots_match_strength"]}\n')
    f.write('    else:\n')
    f.write('        components["cots_match_strength"] = {"value": None, "availability": "NOT_AVAILABLE", "weight": WEIGHTS["cots_match_strength"]}\n')
    f.write('    if standard_consistency is not None:\n')
    f.write('        components["standard_consistency"] = {"value": standard_consistency, "availability": "AVAILABLE", "weight": WEIGHTS["standard_consistency"]}\n')
    f.write('    else:\n')
    f.write('        components["standard_consistency"] = {"value": None, "availability": "NOT_AVAILABLE", "weight": WEIGHTS["standard_consistency"]}\n')
    f.write('    raw_weighted_sum = 0.0\n')
    f.write('    available_weight = 0.0\n')
    f.write('    total_weight = sum(WEIGHTS.values())\n')
    f.write('    for comp in components.values():\n')
    f.write('        if comp["availability"] == "AVAILABLE" and comp["value"] is not None:\n')
    f.write('            raw_weighted_sum += comp["value"] * comp["weight"]\n')
    f.write('            available_weight += comp["weight"]\n')
    f.write('    evidence_coverage = available_weight / total_weight if total_weight > 0 else 0.0\n')
    f.write('    normalized_score = raw_weighted_sum / available_weight if available_weight > 0 else 0.0\n')
    f.write('    score = round(normalized_score, 4)\n')
    f.write('    if evidence_coverage < EVIDENCE_COVERAGE_FLOOR:\n')
    f.write('        reason = f"Evidence coverage {evidence_coverage:.2f} below floor {EVIDENCE_COVERAGE_FLOOR}; forced review"\n')
    f.write('    else:\n')
    f.write('        avail = len([c for c in components.values() if c["availability"] == "AVAILABLE"])\n')
    f.write('        reason = f"Computed from {avail} available components"\n')
    f.write('    return ConfidenceResult(score=score, components=components, evidence_coverage=round(evidence_coverage, 4), formula_version=FORMULA_VERSION, confidence_reason=reason, raw_weighted_sum=round(raw_weighted_sum, 4), normalized_score=round(normalized_score, 4))\n')
print("Created: confidence.py")

# routing.py
with open(os.path.join(SRC_DIR, "routing.py"), "w", encoding="utf-8") as f:
    f.write('"""Review routing logic for recommendations."""\n\n')
    f.write('LOW_CONFIDENCE_THRESHOLD = 0.75\n\n\n')
    f.write('def route_review(confidence_score, evidence_coverage, privacy_class, relationship_evidence, ontology_concept_id, key_role, contradictions, evidence_coverage_floor=0.60):\n')
    f.write('    """Determine if a recommendation needs review and who should review it."""\n')
    f.write('    review_reason = None\n')
    f.write('    reviewer = None\n')
    f.write('    if privacy_class != "INTERNAL":\n')
    f.write('        review_reason = "Privacy classification requires review"\n')
    f.write('        reviewer = "PRIVACY_STEWARD"\n')
    f.write('    elif relationship_evidence is not None:\n')
    f.write('        review_reason = "Inferred relationship requires confirmation"\n')
    f.write('        reviewer = "DATA_ARCHITECT"\n')
    f.write('    elif ontology_concept_id is None:\n')
    f.write('        review_reason = "No approved ontology concept mapped"\n')
    f.write('        reviewer = "DOMAIN_STEWARD"\n')
    f.write('    elif contradictions is not None:\n')
    f.write('        review_reason = "Conflicting evidence requires resolution"\n')
    f.write('        reviewer = "DOMAIN_STEWARD"\n')
    f.write('    elif evidence_coverage < evidence_coverage_floor:\n')
    f.write('        review_reason = f"Evidence coverage {evidence_coverage:.2f} below floor {evidence_coverage_floor}"\n')
    f.write('        reviewer = "DOMAIN_STEWARD"\n')
    f.write('    elif confidence_score < LOW_CONFIDENCE_THRESHOLD:\n')
    f.write('        review_reason = "Confidence below configured threshold"\n')
    f.write('        reviewer = "DOMAIN_STEWARD"\n')
    f.write('    if review_reason is None:\n')
    f.write('        return {"review_reason": None, "recommended_reviewer_role": None}\n')
    f.write('    return {"review_reason": review_reason, "recommended_reviewer_role": reviewer}\n')
print("Created: routing.py")

