# relationships.py
with open(os.path.join(SRC_DIR, "relationships.py"), "w", encoding="utf-8") as f:
    f.write('"""Relationship inference from key naming and type patterns."""\n\n')
    f.write('PRIMARY_KEYS = {\n')
    f.write('    "bronze_policy": "policy_id",\n')
    f.write('    "bronze_policyholder": "party_id",\n')
    f.write('    "bronze_claim": "claim_id",\n')
    f.write('}\n\n')
    f.write('KNOWN_RELATIONSHIPS = {\n')
    f.write('    ("bronze_claim", "policy_id"): ("bronze_policy", "policy_id"),\n')
    f.write('    ("bronze_policy", "insured_party_id"): ("bronze_policyholder", "party_id"),\n')
    f.write('}\n\n\n')
    f.write('def infer_key_role(table_name: str, column_name: str) -> str:\n')
    f.write('    """Infer the key role of a column."""\n')
    f.write('    if PRIMARY_KEYS.get(table_name) == column_name:\n')
    f.write('        return "PRIMARY_KEY_CANDIDATE"\n')
    f.write('    if column_name.endswith("_id"):\n')
    f.write('        return "FOREIGN_KEY_CANDIDATE"\n')
    f.write('    return "NON_KEY"\n\n\n')
    f.write('def infer_relationship(table_name: str, column_name: str):\n')
    f.write('    """Infer relationship evidence for a column."""\n')
    f.write('    key = (table_name, column_name)\n')
    f.write('    if key in KNOWN_RELATIONSHIPS:\n')
    f.write('        target_table, target_column = KNOWN_RELATIONSHIPS[key]\n')
    f.write('        return f"Inferred relationship: {table_name}.{column_name} -> {target_table}.{target_column}"\n')
    f.write('    return None\n\n\n')
    f.write('def compute_relationship_strength(table_name: str, column_name: str, has_relationship: bool) -> float:\n')
    f.write('    """Compute relationship evidence strength."""\n')
    f.write('    if has_relationship:\n')
    f.write('        return 0.9\n')
    f.write('    if column_name.endswith("_id"):\n')
    f.write('        return 0.5\n')
    f.write('    return 0.4\n')
print("Created: relationships.py")

