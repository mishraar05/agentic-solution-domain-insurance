    f.write('}\n\n\n')
    f.write('def check_type_compatibility(physical_type: str, column_name: str) -> float:\n')
    f.write('    """Check if physical type is compatible with column name semantics.\n')
    f.write('    Returns type_strength (0.0 to 1.0).\n')
